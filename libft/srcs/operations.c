int		divide(int a, int b)
{
	return (a / b);
}

int		plus(int a, int b)
{
	return (a + b);
}

int		min(int a, int b)
{
	return (a - b);
}

int		mult(int a, int b)
{
	return (a * b);
}

int		mod(int a, int b)
{
	return (a % b);
}
